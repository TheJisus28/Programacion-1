public class Materia {
    
    private String materia, codigoMateria;
    private int  numDeCreditos, SemestreDeLaMateria;

    public String getMateria() {
        return materia;
    }

    public void setMateria(String materia) {
        this.materia = materia;
    }

    public String getCodigoMateria() {
        return codigoMateria;
    }

    public void setCodigoMateria(String codigoMateria) {
        this.codigoMateria = codigoMateria;
    }

    public int getNumDeCreditos() {
        return numDeCreditos;
    }

    public void setNumDeCreditos(int numDeCreditos) {
        this.numDeCreditos = numDeCreditos;
    }

    public int getSemestreDeLaMateria() {
        return SemestreDeLaMateria;
    }

    public void setSemestreDeLaMateria(int SemestreDeLaMateria) {
        this.SemestreDeLaMateria = SemestreDeLaMateria;
    }
    
    
      
    
}
