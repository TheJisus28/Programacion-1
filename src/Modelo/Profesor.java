public class Profesor {
    
    private String nombreProfesor, tituloDelProfesor,tipoDeVinculacion, codigoProfesor;
    

    public String getNombreProfesor() {
        return nombreProfesor;
    }

    public void setNombreProfesor(String nombreProfesor) {
        this.nombreProfesor = nombreProfesor;
    }

    public String getTituloDelProfesor() {
        return tituloDelProfesor;
    }

    public void setTituloDelProfesor(String tituloDelProfesor) {
        this.tituloDelProfesor = tituloDelProfesor;
    }

    public String getTipoDeVinculacion() {
        return tipoDeVinculacion;
    }

    public void setTipoDeVinculacion(String tipoDeVinculacion) {
        this.tipoDeVinculacion = tipoDeVinculacion;
    }

    public String getCodigoProfesor() {
        return codigoProfesor;
    }

    public void setCodigoProfesor(String codigoProfesor) {
        this.codigoProfesor = codigoProfesor;
    }  
    
}
