    private void estudiante_btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_estudiante_btn_guardarActionPerformed
        // guardamos los datos del estudiante
        
        System.out.println("guardando estudiante....");
        
        estudiante.setNombre(estudiante_txt_nombre.getText());
        estudiante.setEdad(Integer.parseInt(estudiante_txt_edad.getText()));
        estudiante.setIdentificacion(estudiante_txt_identificacion.getText());
        estudiante.setDireccion(estudiante_txt_direccion.getText());
        estudiante.setTelefono(estudiante_txt_telefono.getText());
        estudiante.setCorreo(estudiante_txt_correo.getText());
        estudiante.setCodigoEstudiante(estudiante_txt_codigo.getText());
        
        // Sexo
        javax.swing.JRadioButton selectedRadioButton = null;
        
        if (estudiante_jRadioButton_masculino.isSelected()) 
        {
            selectedRadioButton = estudiante_jRadioButton_masculino;
        }
        else if (estudiante_jRadioButton_femenino.isSelected()) 
        {
            selectedRadioButton = estudiante_jRadioButton_femenino;
        }
        else if (estudiante_jRadioButton_nobinario.isSelected()) 
        {
            selectedRadioButton = estudiante_jRadioButton_nobinario;
        }
        
        estudiante.setSexo(selectedRadioButton.getText());
        System.out.println(estudiante.getSexo());
    }//GEN-LAST:event_estudiante_btn_guardarActionPerformed

