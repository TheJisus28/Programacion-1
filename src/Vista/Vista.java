    private void profesor_btn_guardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_profesor_btn_guardar1ActionPerformed
        // GUARDAMOS LA INFO DE LA MATERIA
        
        materia.setMateria(materia_txt_nombre.getText());
        materia.setCodigoMateria(materia_txt_codigo.getText());
        materia.setNumDeCreditos(Integer.parseInt((String) materia_jComboBox_num_creditos.getSelectedItem()));
        materia.setSemestreDeLaMateria((Integer.parseInt((String) materia_jComboBox_semestre.getSelectedItem())));
        
        // agregamos la materia a la matricula
        matricula_jComboBox_materia.addItem(materia.getMateria());
        
        nota_jComboBox_profesor.addItem(profesor.getNombreProfesor()+" - " + profesor.getCodigoProfesor());
        nota_jComboBox_materia.addItem(materia.getMateria() + " - " + materia.getCodigoMateria());
        nota_jComboBox_estudiante.addItem(estudiante.getNombre() + " - " + estudiante.getCodigoEstudiante());
        
    }//GEN-LAST:event_profesor_btn_guardar1ActionPerformed

